def get_text(key: str, language: str = 'fr') -> str:
    """
    Récupère le texte traduit pour une clé donnée

    Args:
        key: Clé de traduction
        language: Code de langue (fr, en, es)

    Returns:
        Texte traduit
    """
    if language not in TRANSLATIONS:
        language = 'fr'

    # Gérer les clés avec placeholders
    text = TRANSLATIONS[language].get(key, key)

    # Pour les textes avec {num} ou {count}, retourner tel quel
    # car ils seront formatés lors de l'utilisation
    return text


def get_available_languages() -> list:
    """
    Retourne la liste des langues disponibles

    Returns:
        Liste des codes de langue
    """
    return list(TRANSLATIONS.keys())


def format_text(key: str, language: str = 'fr', **kwargs) -> str:
    """
    Récupère et formate un texte traduit avec des paramètres

    Args:
        key: Clé de traduction
        language: Code de langue
        **kwargs: Paramètres pour le formatage

    Returns:
        Texte traduit et formaté
    """
    text = get_text(key, language)
    try:
        return text.format(**kwargs)
    except (KeyError, ValueError):
        return text


def validate_translations() -> dict:
    """
    Valide que toutes les traductions ont les mêmes clés

    Returns:
        Résultat de la validation
    """
    result = {
        'valid': True,
        'missing_keys': {},
        'extra_keys': {},
        'summary': {}
    }

    if not TRANSLATIONS:
        result['valid'] = False
        result['error'] = 'No translations found'
        return result

    # Prendre les clés de la première langue comme référence
    reference_lang = list(TRANSLATIONS.keys())[0]
    reference_keys = set(TRANSLATIONS[reference_lang].keys())

    for lang, translations in TRANSLATIONS.items():
        if lang == reference_lang:
            continue

        current_keys = set(translations.keys())

        # Clés manquantes
        missing = reference_keys - current_keys
        if missing:
            result['valid'] = False
            result['missing_keys'][lang] = list(missing)

        # Clés supplémentaires
        extra = current_keys - reference_keys
        if extra:
            result['extra_keys'][lang] = list(extra)

        # Résumé
        result['summary'][lang] = {
            'total_keys': len(current_keys),
            'missing_count': len(missing),
            'extra_count': len(extra)
        }

    return result